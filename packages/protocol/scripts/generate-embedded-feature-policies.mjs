#!/usr/bin/env node

import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

function parseArgs(argv) {
  const kv = new Map();
  const flags = new Set();
  for (let i = 0; i < argv.length; i += 1) {
    const raw = argv[i];
    if (!raw.startsWith('--')) continue;
    if (raw.includes('=')) {
      const [k, v] = raw.split('=');
      kv.set(k, v ?? '');
      continue;
    }
    const next = argv[i + 1];
    if (next && !next.startsWith('--')) {
      kv.set(raw, next);
      i += 1;
      continue;
    }
    flags.add(raw);
  }
  return { kv, flags };
}

function normalizePolicyEnv(raw) {
  const normalized = String(raw ?? '').trim().toLowerCase();
  if (normalized === 'production' || normalized === 'prod' || normalized === 'stable') return 'production';
  if (normalized === 'preview') return 'preview';
  return null;
}

function normalizeCsv(list) {
  if (!Array.isArray(list)) return '';
  const out = [];
  const seen = new Set();
  for (const value of list) {
    if (typeof value !== 'string') continue;
    const trimmed = value.trim();
    if (!trimmed) continue;
    if (seen.has(trimmed)) continue;
    seen.add(trimmed);
    out.push(trimmed);
  }
  return out.join(',');
}

function parseJson(raw, label) {
  try {
    return JSON.parse(raw);
  } catch (error) {
    throw new Error(`[protocol] invalid JSON for ${label}: ${error instanceof Error ? error.message : String(error)}`);
  }
}

async function loadPolicy(path, label) {
  const doc = parseJson(await readFile(path, 'utf8'), label);
  const buildPolicy = doc?.buildPolicy ?? null;
  return {
    allowRaw: normalizeCsv(buildPolicy?.allow),
    denyRaw: normalizeCsv(buildPolicy?.deny),
  };
}

function renderModuleSource(params) {
  const preview = params.preview;
  const production = params.production;
  const defaultEnv = params.defaultEnv;

  return `// This file is generated by packages/protocol/scripts/generate-embedded-feature-policies.mjs
// Do not edit by hand.

export const DEFAULT_EMBEDDED_FEATURE_POLICY_ENV = ${JSON.stringify(defaultEnv)} as const;

const EMBEDDED_FEATURE_BUILD_POLICY_RAW_INNER = {
  preview: {
    allowRaw: ${JSON.stringify(preview.allowRaw)},
    denyRaw: ${JSON.stringify(preview.denyRaw)},
  },
  production: {
    allowRaw: ${JSON.stringify(production.allowRaw)},
    denyRaw: ${JSON.stringify(production.denyRaw)},
  },
} as const;

export const EMBEDDED_FEATURE_BUILD_POLICY_RAW = Object.freeze({
  preview: Object.freeze({ ...EMBEDDED_FEATURE_BUILD_POLICY_RAW_INNER.preview }),
  production: Object.freeze({ ...EMBEDDED_FEATURE_BUILD_POLICY_RAW_INNER.production }),
}) as typeof EMBEDDED_FEATURE_BUILD_POLICY_RAW_INNER;
`;
}

async function main() {
  const here = dirname(fileURLToPath(import.meta.url));
  const repoRoot = resolve(here, '..', '..', '..');
  const { kv } = parseArgs(process.argv.slice(2));

  const policyDir = String(kv.get('--policy-dir') ?? '').trim() || join(repoRoot, '.github', 'feature-policy');
  const outPath = String(kv.get('--out') ?? '').trim() || join(repoRoot, 'packages', 'protocol', 'src', 'features', 'embeddedFeaturePolicies.generated.ts');
  const requirePolicies = String(process.env.HAPPIER_REQUIRE_FEATURE_POLICY ?? '').trim() === '1';

  const defaultEnv = normalizePolicyEnv(process.env.HAPPIER_EMBEDDED_POLICY_ENV) ?? 'preview';

  const previewPath = join(policyDir, 'preview.json');
  const productionPath = join(policyDir, 'production.json');

  let preview;
  let production;
  try {
    preview = await loadPolicy(previewPath, 'preview');
    production = await loadPolicy(productionPath, 'production');
  } catch (error) {
    if (requirePolicies) throw error;
    preview = { allowRaw: '', denyRaw: '' };
    production = { allowRaw: '', denyRaw: '' };
  }

  const source = renderModuleSource({
    preview,
    production,
    defaultEnv,
  });

  await mkdir(dirname(outPath), { recursive: true });

  const previous = await readFile(outPath, 'utf8').catch(() => null);
  if (previous === source) return;

  await writeFile(outPath, source, 'utf8');
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
});
